"""GeneratedCode: structured result type returned by generate()."""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class GeneratedCode:
    """Structured result of :func:`generate`.

    The generated C code is split into four logical parts so that embedders
    can insert individual sections into an existing code-generation pipeline.

    Attributes
    ----------
    includes:
        Required C header names (without angle brackets), e.g.
        ``["stddef.h", "stdbool.h", "stdint.h"]``.
    globals:
        Global variable and table definitions (``static const`` arrays such as
        the transition table, optional alphabet map, and optional row-index
        map).
    match_function:
        The ``{prefix}_match`` function together with its metadata comment.
    main_function:
        The ``main()`` function, or ``None`` when *emit_main* was ``False``.
    """

    includes: list[str]
    globals: str
    match_function: str
    main_function: str | None = field(default=None)

    def render(self) -> str:
        """Assemble all parts into a single C source string.

        The returned string is identical to the string that :func:`generate`
        returned before the structured result type was introduced.
        """
        parts = ["/* Generated by emx-regex-cgen – do not edit. */", ""]
        parts.extend(f"#include <{inc}>" for inc in self.includes)
        parts.append("")
        parts.append(self.globals)
        parts.append("")
        parts.append(self.match_function)
        if self.main_function is not None:
            parts.append("")
            parts.append(self.main_function)
        parts.append("")
        return "\n".join(parts)
