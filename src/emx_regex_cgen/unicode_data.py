# ---------------------------------------------------------------------------
# Category range computation (from unicodedata, cached)
# ---------------------------------------------------------------------------

@_lru_cache(maxsize=64)
def _category_ranges(cat: str) -> list[tuple[int, int]]:
    """Return sorted, merged ``(lo, hi)`` ranges for general category *cat*.

    *cat* is a 1-letter major category (e.g. ``"L"``) or a 2-letter
    sub-category (e.g. ``"Lu"``).
    """
    one_letter = len(cat) == 1
    ranges: list[tuple[int, int]] = []
    start: int | None = None
    for cp in range(0x110000):
        c = _ud.category(chr(cp))
        match = (c.startswith(cat) if one_letter else c == cat)
        if match:
            if start is None:
                start = cp
        else:
            if start is not None:
                ranges.append((start, cp - 1))
                start = None
    if start is not None:
        ranges.append((start, 0x10FFFF))
    return ranges


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def resolve_property(name: str) -> list[tuple[int, int]]:
    """Return a sorted list of ``(lo, hi)`` code-point ranges for *name*.

    Raises :class:`re.error` when *name* is not a recognised property.
    """
    # 1. Check for special properties.
    if name == "Any":
        return [(0, 0x10FFFF)]
    if name == "ASCII":
        return [(0, 0x7F)]

    # 2. Resolve long category alias -> short code.
    resolved = _CATEGORY_ALIASES.get(name, name)

    # 3. Try general category (1- or 2-letter code).
    if resolved in _GENERAL_CATEGORIES_1 or resolved in _GENERAL_CATEGORIES_2:
        return _category_ranges(resolved)

    # 4. Try script name (case-sensitive, matching Unicode standard).
    if name in _SCRIPT_RANGES:
        return _SCRIPT_RANGES[name]

    raise _re.error(f"unknown Unicode property name: {name!r}")
